void bfs(int i, int j){
	queue<ii> q;
	q.push({i, j});
	a[i][j] = 0;
	while(!q.empty()){
		ii u = q.front(); q.pop();
		if(u.fi == t && u.se == v) return true;
		for(int k = 0; k < 4; k++){
			int i1 = u.fi + dx[k];
			int j1 = u.se + dy[k];
			if(check(i1,j1) && a[i1][j1]){
				q.push({i1,j1});
				a[i1][j1] = 0;
			}
		}
	}
	return false;
}
